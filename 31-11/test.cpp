struct TestCase
{
    Hash::Mode Mode;
    std::vector<uint8_t> input;
    std::vector<uint8_t> expected;
};

class ProcessInputTest : public ::testing::TestWithParam<TestCase> {};

TEST_P(ProcessInputTest, ReturnsExpectedResult) {
    const auto& param = GetParam();

    std::istringstream iss(std::string(param.input.begin(), param.input.end()));
    std::ostringstream oss;

    //std::cout << iss.str() << std::endl;

    Hash::GetHash(iss, oss, param.Mode);
    
    EXPECT_EQ(oss.str(), std::string(param.expected.begin(), param.expected.end()));
}

