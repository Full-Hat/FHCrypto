Sequence::Sequence(uint1024_t P, uint1024_t A, uint1024_t B, uint1024_t E, uint1024_t D,
				   uint1024_t M, uint1024_t Q, uint1024_t X, uint1024_t Y, uint1024_t U,
			 	   uint1024_t V)
{
	p = P;
	a = A;
	b = B;
	e = E;
	d = D;
	m = M;
	q = Q;
	x = X;
	y = Y;
	u = U;
	v = V;
}

Sequence::Sequence(const Sequence &pset)
{	

	p = pset.p;
	a = pset.a;
	b = pset.b;
	e = pset.e;
	d = pset.d;
	m = pset.m;
	q = pset.q;
	x = pset.x;
	y = pset.y;
	u = pset.u;
	v = pset.v;
			
}

